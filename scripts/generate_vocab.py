"""
FlashWords vocabulary generator.

Produces public/flashcards.json from a CURATED, grammatically-correct dataset.
Unlike the old generator, there are NO machine-built "phrase" cards with broken
grammar. Every entry below is hand-written:
  - nouns carry their correct gender (en / ei / et)
  - every card has a real, idiomatic Norwegian example sentence + English gloss

The dataset is split into batches so it can grow toward ~1000 cards over time
while keeping quality high. Run:  python scripts/generate_vocab.py
It writes ../public/flashcards.json (ids are stable, derived from the word).
"""

import json
