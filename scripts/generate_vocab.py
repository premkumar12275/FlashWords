def build():
    cards = []
    for no, en, g, ex_no, ex_en in NOUNS + NOUNS_B + NOUNS_C + NOUNS_D:
        cards.append(card(no, en, "noun", ex_no, ex_en, gender=g))
    for root, en, ex_no, ex_en in VERBS + VERBS_B + VERBS_C:
        cards.append(card(f"å {root}", f"to {en}", "verb", ex_no, ex_en))
    for no, en, ex_no, ex_en in ADJECTIVES + ADJECTIVES_B + ADJECTIVES_C:
        cards.append(card(no, en, "adjective", ex_no, ex_en))
    for no, en, pos, ex_no, ex_en in OTHER + OTHER_B:
        cards.append(card(no, en, pos, ex_no, ex_en))
    for no, en in PHRASES + PHRASES_B + PHRASES_C:
        cards.append(card(no, en, "phrase", no, en, level="intermediate"))

    # de-duplicate by id, keeping first occurrence
    seen = {}
    for c in cards:
        seen.setdefault(c["id"], c)
    return list(seen.values())


