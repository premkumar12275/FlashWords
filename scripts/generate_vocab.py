import uuid

NAMESPACE = uuid.UUID("6b3c2f1a-0d4e-4a8b-9c11-7e2a5d6f8b00")


def stable_id(norwegian: str, pos: str) -> str:
    """Deterministic id so re-running doesn't churn ids (good for git diffs)."""
    return str(uuid.uuid5(NAMESPACE, f"{pos}:{norwegian}"))


def card(no, en, pos, ex_no, ex_en, gender=None, level="beginner"):
    c = {
        "id": stable_id(no, pos),
        "norwegianWord": no,
        "englishMeaning": en,
        "partOfSpeech": pos,
        "exampleNorwegian": ex_no,
        "exampleEnglish": ex_en,
        "difficultyLevel": level,
    }
    if gender:
        c["gender"] = gender
    return c


